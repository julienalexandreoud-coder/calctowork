# Patch each language file
# ---------------------------------------------------------------------------

LANGS = ["es", "en", "fr", "pt", "de", "it"]

for lang in LANGS:
    path = I18N / f"{lang}.json"
    data = json.loads(path.read_text(encoding="utf-8"))

    # 1. block_slugs
    data.setdefault("block_slugs", {}).update(BLOCK_SLUGS[lang])

    # 2. block_descriptions
    data.setdefault("block_descriptions", {}).update(BLOCK_DESCRIPTIONS[lang])

    # 3. calculators
    calcs = data.setdefault("calculators", {})
    for calc_id, langs_data in CALCULATORS.items():
        entry = langs_data[lang]
        calcs[calc_id] = {
            "name":        entry["name"],
            "desc":        entry["desc"],
            "description": entry["desc"],
            "seo_title":   entry["seo_title"],
            "seo_description": entry["seo_desc"],
            "inputs":      entry["inputs"],
            "outputs":     entry["outputs"],
        }

    # Write without BOM
    import io
    content = json.dumps(data, ensure_ascii=False, indent=2)
    path.write_bytes(content.encode("utf-8"))
    print(f"[OK] {lang}.json patched — {len(calcs)} total calculators")

print("\nDone. Run the generator next.")
